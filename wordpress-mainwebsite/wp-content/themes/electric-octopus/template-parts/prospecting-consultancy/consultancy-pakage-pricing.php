  <script>
    // Dropdown behaviour
    (function(){
      const btn = document.getElementById('currencyBtn');
      const list = document.getElementById('currencyList');
      const label = document.getElementById('currencyLabel');
      const items = document.querySelectorAll('.currency-item');
      const pkg1 = document.getElementById('pkg1-curr');
      const pkg2 = document.getElementById('pkg2-curr');
      const pkg3 = document.getElementById('pkg3-curr');

      btn.addEventListener('click', function(e){
        const open = list.classList.toggle('open');
        list.setAttribute('aria-hidden', !open);
        btn.setAttribute('aria-expanded', open);
      });

      // close dropdown on click outside
      document.addEventListener('click', function(ev){
        if (!document.getElementById('currencyWrap').contains(ev.target)) {
          list.classList.remove('open');
          list.setAttribute('aria-hidden', 'true');
          btn.setAttribute('aria-expanded', 'false');
        }
      });

      items.forEach(it=>{
        it.addEventListener('click', function(){
          const txt = this.innerText.trim();
          const sym = this.getAttribute('data-symbol') || '$';
          const img = this.querySelector('img').src;
          label.innerText = txt;
          // change flag icon
          btn.querySelector('img').src = img;

          // update currency symbols in price display
          if(pkg1) pkg1.innerText = sym;
          if(pkg2) pkg2.innerText = sym;
          if(pkg3) pkg3.innerText = sym;

          // close
          list.classList.remove('open');
          list.setAttribute('aria-hidden', 'true');
          btn.setAttribute('aria-expanded', 'false');
        });
      });
    })();
  </script>